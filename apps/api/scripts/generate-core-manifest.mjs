#!/usr/bin/env node
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import {
  fetchProblemIndexRows,
  fetchQuestionData,
  getElementFromTags,
  mapDifficulty,
  mapWithConcurrency,
  writeJson,
} from './core-manifest-tools.mjs';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const TARGET_COUNT = 300;
const CANDIDATE_POOL_SIZE = 750;
const COVERAGE_MIN_PER_ELEMENT = 30;
const ELEMENTS = ['Array', 'Graph', 'Tree', 'Math', 'DynamicProgramming', 'String'];

function toCandidateRow(row) {
  const slug = row?.stat?.question__title_slug;
  const title = row?.stat?.question__title;
  const hidden = Boolean(row?.stat?.question__hide);
  const paidOnly = Boolean(row?.paid_only ?? row?.stat?.question__paid_only);
  const totalAcs = Number(row?.stat?.total_acs ?? 0);
  const totalSubmitted = Number(row?.stat?.total_submitted ?? 0);
  const difficulty = mapDifficulty(Number(row?.difficulty?.level));

  if (!slug || !title || hidden || paidOnly) return null;

  return {
    slug,
    title,
    difficulty,
    totalAcs,
    totalSubmitted,
    acceptanceRate: totalSubmitted > 0 ? totalAcs / totalSubmitted : 0.5,
  };
}

function formatCatalogVersion() {
  const d = new Date();
  const yyyy = d.getUTCFullYear();
  const mm = `${d.getUTCMonth() + 1}`.padStart(2, '0');
  const dd = `${d.getUTCDate()}`.padStart(2, '0');
  return `core-${yyyy}${mm}${dd}-v1`;
}

function createTsManifestModule(manifest) {
  return [
    "// Auto-generated by scripts/generate-core-manifest.mjs",
    `export const CORE_CATALOG_MANIFEST = ${JSON.stringify(manifest, null, 2)} as const;`,
    '',
  ].join('\n');
}

async function main() {
  console.log('[manifest] Fetching LeetCode index...');
  const rows = await fetchProblemIndexRows();

  const candidates = rows
    .map(toCandidateRow)
    .filter(Boolean)
    .sort((a, b) => b.totalAcs - a.totalAcs)
    .slice(0, CANDIDATE_POOL_SIZE);

  console.log(`[manifest] Resolving metadata for ${candidates.length} candidates...`);
  const enriched = await mapWithConcurrency(candidates, 8, async (candidate) => {
    try {
      const question = await fetchQuestionData(candidate.slug);
      if (!question || question.isPaidOnly) return null;

      const tags = Array.isArray(question.topicTags)
        ? question.topicTags.map((t) => t.slug).filter(Boolean)
        : [];
      const difficulty =
        question.difficulty === 'Easy' || question.difficulty === 'Hard'
          ? question.difficulty
          : 'Medium';
      const acceptanceRate = typeof question.acRate === 'number'
        ? question.acRate / 100
        : candidate.acceptanceRate;

      return {
        title_slug: question.titleSlug ?? candidate.slug,
        title: question.title ?? candidate.title,
        difficulty,
        tags,
        acceptance_rate: Number(Math.max(0, Math.min(1, acceptanceRate)).toFixed(4)),
        is_free: true,
        popularity_score: candidate.totalAcs,
        primary_element: getElementFromTags(tags),
      };
    } catch {
      return null;
    }
  });

  const valid = enriched.filter(Boolean);
  if (valid.length < TARGET_COUNT) {
    throw new Error(`[manifest] Only ${valid.length} valid free candidates resolved; need ${TARGET_COUNT}.`);
  }

  const selected = [];
  const selectedSlugs = new Set();

  // Pass 1: enforce minimum element coverage.
  for (const element of ELEMENTS) {
    const pool = valid
      .filter((item) => item.primary_element === element)
      .sort((a, b) => b.popularity_score - a.popularity_score);

    for (const item of pool) {
      if (selected.length >= TARGET_COUNT) break;
      if (selectedSlugs.has(item.title_slug)) continue;
      if (selected.filter((x) => x.primary_element === element).length >= COVERAGE_MIN_PER_ELEMENT) break;

      selected.push(item);
      selectedSlugs.add(item.title_slug);
    }
  }

  // Pass 2: fill remaining slots by popularity.
  for (const item of valid.sort((a, b) => b.popularity_score - a.popularity_score)) {
    if (selected.length >= TARGET_COUNT) break;
    if (selectedSlugs.has(item.title_slug)) continue;
    selected.push(item);
    selectedSlugs.add(item.title_slug);
  }

  if (selected.length < TARGET_COUNT) {
    throw new Error(`[manifest] Selection ended at ${selected.length}; expected ${TARGET_COUNT}.`);
  }

  const catalogVersion = formatCatalogVersion();
  const items = selected.slice(0, TARGET_COUNT).map((item) => ({
    title_slug: item.title_slug,
    title: item.title,
    difficulty: item.difficulty,
    tags: item.tags,
    acceptance_rate: item.acceptance_rate,
    is_free: true,
    catalog_version: catalogVersion,
  }));

  const manifest = {
    catalog_version: catalogVersion,
    generated_at: new Date().toISOString(),
    selection_strategy: {
      target_count: TARGET_COUNT,
      candidate_pool_size: CANDIDATE_POOL_SIZE,
      minimum_per_element: COVERAGE_MIN_PER_ELEMENT,
      ranking: 'total accepted submissions',
    },
    items,
  };

  const jsonPath = path.resolve(__dirname, '../catalog/core-manifest.v1.json');
  const tsPath = path.resolve(__dirname, '../src/catalog/core-manifest.v1.ts');

  writeJson(jsonPath, manifest);
  writeJson(path.resolve(__dirname, '../catalog/reports/core-manifest.generation-summary.json'), {
    catalog_version: catalogVersion,
    generated_at: manifest.generated_at,
    selected_count: items.length,
    element_counts: ELEMENTS.reduce((acc, element) => {
      acc[element] = selected.filter((x) => x.primary_element === element).length;
      return acc;
    }, {}),
  });

  const tsModule = createTsManifestModule(manifest);
  await import('node:fs').then((fs) => {
    fs.mkdirSync(path.dirname(tsPath), { recursive: true });
    fs.writeFileSync(tsPath, `${tsModule}\n`, 'utf8');
  });

  console.log(`[manifest] Generated ${items.length} entries at ${jsonPath}`);
  console.log(`[manifest] TS module generated at ${tsPath}`);
}

main().catch((err) => {
  console.error(err instanceof Error ? err.message : err);
  process.exit(1);
});
